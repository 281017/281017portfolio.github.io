          <div class="text-spacing"></div>
          <p class="infographic">
          Jeg startede på SDE som snedker og var der i 1 måned, hvorefter jeg skiftede til tømrer hvor jeg fandt noget som interesserede mig. Efter grundforløbet kom jeg i lærer hos et tømrerfirma ved navnet LY-BYG og der blev jeg så udlært og kom igennem skolen uden problemer jeg fik 12. taller til alle oprykningsprøverne og til svendeprøven fik jeg 10 i den praktiske prøve, og 12 i den teoretiske prøve. På daværende tidspunkt fandt jeg ud af at jeg ikke rigtigt var interesseret i selve arbejdet, men at skolearbejdet var det interessante. Det med at finde ud af om tingene kunne lade sig gøre og tegne dem. Så jeg manglede bare det sidste skub. Jeg fandt så ud af at en af mine kollegaer havde været Bygningskonstruktør og en samtale senere meldte jeg mig til Bygningskonstruktør linjen på VIA UNIVERSITY HORSENS jeg arbejdede som svend i et 1 år før jeg startede min BK-uddannelse. Nu er jeg på 3. semester som bygningskonstruktør og jeg må sige jeg er fanget af faget, jeg ser frem til de opkommende udfordringer som helt klart vil komme de næste semestre.</p>
