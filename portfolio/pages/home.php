          <div class="text-spacing"></div>
          <p class="infographic" id="trigger-logo">Jeg startede i folkeskole ligesom mange andre men jeg endte med at droppe ud i 8. klasse pga. jeg ikke mente jeg fik den hjælp jeg skulle have, til at kunne gennemføre 9. klasse. Derfor startede jeg på en ungdomsskole i Vejle hvor jeg udviklede mig rigtigt meget ikke bare fysisk men også mentalt og fagligt. Der var jeg i ca. 2 år og lærte en masse snedker og smedearbejde. Jeg lærte at bruge drejebænke til metal og træ. Dette gav mig den første slumrende lyst til at arbejde med faget. Selvom dette endte jeg med at gå i Web og video design retningen da det var min fritidsinteresse og stadig er. Derfor startede jeg på en produktionsskole som går ved navnet Datariet. Jeg gik på en linje ved navnet (digital design) Der lærte jeg meget med videoredigering, billedredigering og en lille smule kodning i form af HTML / CSS der bliver brugt som grundsten i hjemmeside kodning. Derudover har jeg lært at bruge Photoshop og After Effects. Hvilket jeg stadig bruger i min fritid.</p>
          <div class="text-spacing"></div>
          <p class="infographic">
